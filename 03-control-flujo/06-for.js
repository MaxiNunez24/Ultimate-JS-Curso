let i = 2;
while (i < 2){
    if (i % 2 === 0){
        console.log('Nuemro par ',i);
    }
    i++;
}


// El for es una forma resumida de While, aunque se utiliza cuando sabemos la cantidad exacta de veces que queremos que se ejecute
for (let i = 0; i < 10; i++){
    if (i % 2 === 0){
        console.log('Numero par ',i);
    }
}
