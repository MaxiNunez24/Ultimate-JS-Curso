let a = 10;


// Operadores Relacionales
console.log(a > 5); // Mayor que
console.log(a >= 5); // Mayor o igual que
console.log(a < 5); // Menor que
console.log(a <= 5); // Menor o igual que

// Operadores de Igualdad
console.log(a == 10); // Igual que
console.log(a != 10); // Diferente que
console.log(a == '10'); // Igual que  (compara solo el valor no el tipo de dato)
console.log(a === '10'); // Igual que (compara el valor y el tipo de dato)
console.log(a !== '10'); // Diferente que (compara el tipo de dato)